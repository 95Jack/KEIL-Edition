//输出汉字字符串
void LCD_P14x16Str(byte x,byte y,byte ch[])
{
  byte wm=0,ii = 0;
  word adder=1;  
  while(ch[ii] != '\0')
  {
    wm = 0;
    adder = 1;
    while(F14x16_Idx[wm] > 127)
    {
      if(F14x16_Idx[wm] == ch[ii])
      {
        if(F14x16_Idx[wm + 1] == ch[ii + 1])
        {
          adder = wm * 14;
          break;
        }
      }
      wm += 2;			
    }
    if(x>118){x=0;y++;}
    LCD_Set_Pos(x , y); 
    if(adder != 1)// 显示汉字					
    {
      LCD_Set_Pos(x , y);
      for(wm = 0;wm < 14;wm++)               
      {
        LCD_WrDat(F14x16[adder]);	
        adder += 1;
      }      
      LCD_Set_Pos(x,y + 1); 
      for(wm = 0;wm < 14;wm++)          
      {
        LCD_WrDat(F14x16[adder]);
        adder += 1;
      }   		
    }
    else			  //显示空白字符			
    {
      ii += 1;
      LCD_Set_Pos(x,y);
      for(wm = 0;wm < 16;wm++)
      {
        LCD_WrDat(0);
      }
      LCD_Set_Pos(x,y + 1);
      for(wm = 0;wm < 16;wm++)
      {   		
        LCD_WrDat(0);	
      }
    }
    x += 14;
    ii += 2;
  }
}
